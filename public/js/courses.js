        const tutorsGrid = document.getElementById('tutorsGrid');
        const curriculumList = document.getElementById('curriculumList');
        const closeBtn = document.querySelector('.close');
        const hamburger = document.getElementById('hamburger');
        const navLinks = document.getElementById('navLinks');
        const navOverlay = document.querySelector('.nav-overlay');
        const body = document.body;

        // Mobile menu toggle
        hamburger.addEventListener('click', (e) => {
            e.preventDefault();
            e.stopPropagation();
            console.log('Hamburger clicked'); // Debug log
            hamburger.classList.toggle('active');
            navLinks.classList.toggle('active');
            body.classList.toggle('nav-open');
        });

        // Close mobile menu when clicking on overlay
        if (navOverlay) {
            navOverlay.addEventListener('click', () => {
                hamburger.classList.remove('active');
                navLinks.classList.remove('active');
                body.classList.remove('nav-open');
            });
        }

        // Close mobile menu when clicking on a nav link
        document.querySelectorAll('.nav-links a').forEach(link => {
            link.addEventListener('click', () => {
                hamburger.classList.remove('active');
                navLinks.classList.remove('active');
                body.classList.remove('nav-open');
            });
        });

        // Close menu when pressing escape key
        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape') {
                hamburger.classList.remove('active');
                navLinks.classList.remove('active');
                body.classList.remove('nav-open');
            }
        });

        // Subject card click handlers
        document.querySelectorAll('.subject-card').forEach(card => {
            card.addEventListener('click', () => {
                const subject = card.dataset.subject;
                showSubjectDetails(subject);
            });
        });

        // Modal close handlers
        closeBtn.addEventListener('click', () => {
            modal.style.display = 'none';
        });

        window.addEventListener('click', (e) => {
            if (e.target === modal) {
                modal.style.display = 'none';
            }
        });

        // Show subject details
        function showSubjectDetails(subject) {
            const data = subjectData[subject];
            
            // Update modal header
            modalIcon.className = `${data.icon} subject-icon`;
            modalIcon.style.color = data.color;
            modalTitle.textContent = data.title;
            
            // Populate tutors
            tutorsGrid.innerHTML = '';
            data.tutors.forEach(tutor => {
                const tutorCard = document.createElement('div');
                tutorCard.className = 'tutor-card';
                tutorCard.innerHTML = `
                    <div class="tutor-avatar">
                        <i class="fas fa-user-graduate"></i>
                    </div>
                    <h4>${tutor.name}</h4>
                    <p style="display: none;">${tutor.qualification}</p>
                    <p style="display: none;"><strong>Specialty:</strong> ${tutor.specialty}</p>
                    <span class="tutor-experience">${tutor.experience}</span>
                `;
                tutorsGrid.appendChild(tutorCard);
            });
            
            // Populate curriculum
            curriculumList.innerHTML = '';
            data.curriculum.forEach(item => {
                const li = document.createElement('li');
                li.innerHTML = `<i class="fas fa-check"></i> ${item}`;
                curriculumList.appendChild(li);
            });
            
            // Show modal
            modal.style.display = 'block';
        }

        // Scroll animations
        const observerOptions = {
            threshold: 0.1,
            rootMargin: '0px 0px -50px 0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('revealed');
                }
            });
        }, observerOptions);

        document.querySelectorAll('.scroll-reveal').forEach(el => {
            observer.observe(el);
        });

        // Hero background slideshow
        let currentBg = 0;
        const backgrounds = document.querySelectorAll('.hero-background');
        
        function nextBackground() {
            backgrounds[currentBg].classList.remove('active');
            currentBg = (currentBg + 1) % backgrounds.length;
            backgrounds[currentBg].classList.add('active');
        }

        // Change background every 5 seconds
        setInterval(nextBackground, 5000);

        // Navbar scroll effect
        window.addEventListener('scroll', () => {
            const navbar = document.querySelector('.navbar');
            if (window.scrollY > 50) {
                navbar.classList.add('scrolled');
            } else {
                navbar.classList.remove('scrolled');
            }
        });

        // Smooth scrolling for navigation links
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    target.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            });
        });
